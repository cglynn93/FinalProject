    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // save new user info to database
        User user = new User();
        user.setName(signupName.getText());
        user.setEmail(signupEmail.getText());
        user.setMobileNumber(signupMobile.getText());
        user.setPassword(signupPassword.getText());
        user.setQuestion(signupQuestion.getText());
        user.setAnswer(signupAnswer.getText());
        UserDataAccessObject.save(user);
        clear();
    }//GEN-LAST:event_btnSaveActionPerformed

