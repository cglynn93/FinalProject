    public void clear(){
        signupName.setText("");
        signupEmail.setText("");
        signupMobile.setText("");
        signupQuestion.setText("");
        signupAnswer.setText("");
        signupPassword.setText("");
        btnSave.setEnabled(false);
    }
    
    public void validateFields(){
        String name = signupName.getText();
        String email = signupEmail.getText();
        String mobileNumber = signupMobile.getText();
        String password = signupPassword.getText();
        String securityQuestion = signupQuestion.getText();
        String answer = signupAnswer.getText();
        if(!name.equals("") && email.matches(emailPattern) && mobileNumber.matches(mobileNumberPattern) && mobileNumber.length() == 10 && !password.equals("") && !securityQuestion.equals("") && !answer.equals(""))
            btnSave.setEnabled(true);
        else
            btnSave.setEnabled(false);
    }

