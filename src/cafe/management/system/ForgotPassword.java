    public void clear(){
        btnSave.setEnabled(false);
        btnSearch.setEnabled(false);
        forgotEmail.setEditable(true);
        forgotEmail.setText("");
        forgotSecurityQuestion.setText("");
        forgotAnswer.setText("");
        forgotNewPassword.setText("");
    }
    
    public void validateEmail(){
        email = forgotEmail.getText();
        if(email.matches(emailPattern))
            btnSearch.setEnabled(true);
        else
            btnSearch.setEnabled(false);
    }
    
    public void validateFields(){
        String password = forgotNewPassword.getText();
        String answer = forgotAnswer.getText();
        String securityQuestion = forgotSecurityQuestion.getText();
        if(!password.equals("") && !answer.equals("") && !securityQuestion.equals(""))
            btnSave.setEnabled(true);
        else
            btnSave.setEnabled(false);
    }

