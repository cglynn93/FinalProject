    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        email = forgotEmail.getText();
        User user = null;
        user = UserDataAccessObject.getSecurityQuestion(email);
        if (user == null) {
            JOptionPane.showMessageDialog(null, "<html><b style =\"colour:red\">Incorrect Email</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        } else {
            btnSearch.setEnabled(false);
            forgotEmail.setEditable(false);
            dbAnswer = user.getAnswer();
            forgotSecurityQuestion.setText(user.getQuestion());
            validateFields();
        }
    }//GEN-LAST:event_btnSearchActionPerformed

